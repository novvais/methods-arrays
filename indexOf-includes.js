//indexOf() = Enconta um item dentro de um array, retornando o indice da primeiro ocorrencia do item procurado

const lista = ["Eu", "Tu", "Eles"]
const indice = lista.indexOf("Tu")

console.log(indice) //1

//includes() = Verifica se existe um item dentro de um array, retornando true ou false

const lista1 = ["Eu", "Tu", "Eles"]
const boolean = lista1.includes("Tu")

console.log(boolean) //True